<p>Estos día ha tenido lugar en el <a href="http://www.cfreus.net/" target="_blank">Colegio francés de Reus</a>, el encuentro anual de representantes de los <strong>Conseils de </strong><strong>la </strong><strong>Vie Lycéenne </strong>(Consejos de escolares de los alumnos de Lycée)<strong> de los centros educativos de la red Misión laica francesa España,</strong> del <a href="http://www.vhugo.eu/" target="_blank">Lycée Victor Hugo de Florence</a>, del <a href="http://www.mlfmonde.org/etablissements/lycee-osui-jean-charcot/" target="_blank">Lycée Charcot de El Jadida</a> (Maroc), así como de los delegados del Conseil Académique parisino de la Vie Lycéenne.</p>
<p><p class="p3">Acompañados por los CPE (Consejeros principales de educación), por los directores de los liceos de <a href="http://www.lfgrancanaria.com/index.php/es/" target="_blank">Gran Canarias</a> y de Villanueva de la Cañada, y por representantes de la Académie de Paris, los delegados participaron durante tres días en dos talleres: el primero sobre <strong>Democracia</strong> animado por la Federación francófona de Debates, de entrenamiento en el arte de la oratoria y la confrontación verbal; el segundo, propuesto por la asociación Make it real, que tenía por objetivo enseñarles a impulsar <strong>proyectos de desarrollo sostenible</strong> en sus centros educativos.</p></p>
