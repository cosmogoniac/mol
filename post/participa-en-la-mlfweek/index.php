<article class="articulo"><p><img class='centrar' src="http://www.lyceemolieresaragosse.org/wp-content/uploads/2017/02/mlfweek-es-300x142.png"/></p>
<p>Le réseau des établissements de la <strong><a href="http://www.mlfmonde.org/" target="_blank">Mission Laïque Francaise</a></strong> (MLF) en Espagne se met en scène et en ligne avec la <strong><a href="http://www.mlfmonde.org/du-13-au-17-fevrier-2017-participez-sur-les-reseaux-sociaux-a-la-premiere-mlfweek/" target="_blank">MLFWeek</a></strong>, sur le modèle de la Museum Week (une opération qui rassemble chaque année sur la toile des milliers de musées).</p>
<p>Du lundi 13 au vendredi 17 février, les interessés pourront poster chaque jour sur Twitter, Facebook et Instagram, des contenus autour de cinq thématiques choisies. En plus du hashtag #mlfweek, un hashtag thématique a été identifié pour chaque jour de la semaine, afin de partager des histoires, des photos, des informations, des vidéos, etc.</p>
<p>La red de colegios de la Mission Laïque Française (MLF) en España pone en marcha la Mlfweek, siguiendo el modelo de la Museum Week (una campaña que reúne así miles de museos del mundo).</p>
<p>Del lunes 13 al viernes 17 de febrero, quien quiera puede publicar cada día en Twitter, Facebook e Instagram contenidos en torno a cinco temáticas elegidas. Además, el hashtag #mlfweek y un hashtag temático se unirá cada día de la semana a las historias, fotos, información y vídeos.</p>
<p><strong>5 jours, 5 thèmes, 5 hashtags</strong></p>
<p><em><strong>Lundi : #mobilitéinternationale</strong></em></p>
<p>Les écoles de la Mission laïque française, qui font partie intégrante du réseau de l’enseignement français à l’étranger, ouvrent des portes sur le monde. Elles préparent les élèves à vivre pendant et après leur scolarité, des expériences positives et déterminantes de mobilité internationale.</p>
<p><em><strong>Mardi : #langues</strong></em></p>
<p>Le multilinguisme des élèves du réseau mlfmonde est une richesse que les établissements cultivent. Il est aussi la marque d’une véritable éducation internationale.</p>
<p><em><strong>Mercredi : #citoyenneté</strong></em></p>
<p>Les élèves de nos établissements scolaires ont l’avenir entre leurs mains. Les lycées de la Mission laïque les forment à devenir des adultes responsables, éclairés et ouverts sur le monde.</p>
<p><em><strong>Jeudi : #autonomie</strong></em></p>
<p>Rendre les élèves acteurs de leurs apprentissages, c’est un des objectifs principaux que poursuivent nos écoles, collèges et lycées. Pour cela, l’ensemble de la communauté éducative travaille de concert afin de les accompagner dans la construction de leur autonomie.</p>
<p><em><strong>Vendredi : #innovation</strong></em></p>
<p>Pour ce dernier jour de la semaine, place à l’innovation, la marque de fabrique des établissements du réseau mlfmonde qui concentrent leurs efforts pour proposer aux familles une éducation adaptée aux besoins de leurs enfants et fondée sur des pratiques pédagogiques innovantes.</p>
<p>À vos Smarts, prêts ? Partez!</p>
