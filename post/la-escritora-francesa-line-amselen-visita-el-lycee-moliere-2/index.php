La conocida escritora francesa Line Amselen, natural de París, acudirá mañana, día 30 de abril, al Lycée Molière para presentar su novela Pequeñas historias de la calle de San Nicolás, que ha sido editada recientemente en Aragón por el sello aragonés Xordica. Así, a partir de las 10.00 atenderá a los alumnos de 3ème (3º de ESO), y a las 11.00 a los de 2nde (4º de ESO), que le harán preguntas que habrán preparado previamente con sus profesores de francés.

