<p><strong>Red Alumni Molière [RAM]</strong>: María Pilar Alonso · Promoción '86</strong></p>

<p>Después de trabajar y de camino a recoger a sus hijos de las extraescolares, María Pilar Alonso (‘86) hizo parada en el Lycée. En el actual, porque ella junto con sus dos hermanos mayores fue una de las primeras alumnas de la calle Ramdeviu y posteriormente, cuando ese pequeño chalet quedó pequeño, de aquel Molière ‘de la carretera de Logroño’ frente a la torre de Pikolin, como todo el mundo lo conoce.</p>

<p>Ahora María Pilar trabaja como <em>área</em> <em>manager</em> de Fersa Bearings, empresa multinacional con sede central en la plataforma logística de Zaragoza, y fábricas aquí, en China y en Austria, además de delegaciones en Ohio (EEUU) y en Sao Paulo (Brasil).</p>

<p>Esta semana está en Zaragoza, pero la próxima viajará por trabajo. Sus estudios después del Molière se orientaron en este sentido precisamente: una diplomatura de estudios internacionales, un año de gestión comercial y marketing, y un Máster en dirección de comercio internacional.</p>

<p>Entre trabajo, familia y viajes, se detiene unos minutos en aquellos años que pasó en el Molière para traer a la memoria algunos recuerdos.</p>

<p><strong>En toda esta trayectoria de estudios y la actual profesional, ¿qué te ha aportado estudiar en el Lycée français Molière?</strong></p>

<p>Me abrió puertas hacia los idiomas, hacia que yo dirigiera mi carrera al mundo internacional, porque fue mucho más fácil en francés y en inglés. Lo reforcé después del colegio con cursos en la escuela de idiomas para obtener títulos oficiales. Incluso empecé a estudiar alemán.</p>

<p><strong>¿Ser bilingüe te ha servido para aprender el resto de idiomas?</strong></p>

<p>Nunca sabré qué es no saber un idioma desde pequeña, pero yo creo que tenerlo te abre la mente.</p>

<p><strong>¿Qué aprendizaje personal te llevaste del colegio?</strong></p>

<p>Me transmitió valores. Era una versión más laica de lo que se percibía en la época. Yo creo que fue una mirada más real del mundo que me rodeaba. Y todo eso a pesar de que mis padres eran bastante católicos. Creo que aquí se cuida mucho la faceta del ser humano y a partir de ahí se introducen los conocimientos. Intenta sacar lo mejor de nosotros mismos.</p>

<p><strong>¿Agradeces la elección de tus padres para ti y tus hermanos?</strong></p>

<p>Sí, mucho, porque además en esos tiempos fue realmente un gran esfuerzo para ellos. Los tres hermanos tenemos carreras universitarias. Mi hermana trabaja en Toulouse para el mundo de la aeronáutica y mi hermano estudió ingeniería superior y trabaja como consultor.</p>

<p><strong>¿Qué recuerdos guardas de tu época en el Molière?</strong></p>

<p>Me acordaré toda la vida de Madame Oger (o 'la ogeras' como la llamábamos los alumnos por su apellido) y de Madame Valls. Esta última era buenísima como profesora de idiomas. Nos daba inglés y alemán y me acuerdo perfectamente que nos ponía películas en V.O. y se curraba todos los diálogos para transcribírnoslos a mano y que pudiéramos seguir sin problemas. Me acordaré toda mi vida de la peli de E.T. que veíamos de 20 minutos en 20 minutos, analizando los diálogos clase tras clase.</p>

<p>Recuerdo a Madame García y su voz ronca, siempre amable. Y a Monsieur Guireaud (profesor de Física y Química), que nos daba clases prácticas enseñándonos cómo funcionaba un motor con su propia moto que abría para que le viéramos las tripas. Recuerdo a Madame Viviente como profe de español y a Monsieur Chopin profe de francés con el que muchos debutamos como actores en la Troupe du Moliére (pequeña compañía de teatro en la que representábamos obras de Moliére sobre todo, y con las que a veces viajábamos para representar funciones en pueblos de la zona). Y, sobre todo, recuerdo a Mr. Echegoyen 'el dire', que nos llamaba a su despacho cuando había que meter una bronca… Era como un padre… Pero claro entonces éramos como una familia: en el último año de mi promoción éramos cinco alumnos en clase. Lo dicho. una familia.</p>

<p><strong>¿Alguna anécdota divertida?</strong></p>

<p>José el cocinero del Moliére, por aquel entonces, tenía la mesa de los mayores al lado de la cocina. Se levantaba bien prontito para hacer él mismo todo de manera casi artesanal, hasta la repostería. Cada año, los mayores (los que estaban en el último curso) gozaban del privilegio de repetir los postres que sobraban. Era como un premio especial que todos ansiábamos. Y hasta nos dio charlas, junto con otros profesionales que hicieron lo mismo, sobre ser cocinero, sobre todo lo que había que hacer con <em>tour</em> por la cocina y una demostración práctica, con cata posterior. A estas charlas vinieron profesionales de todos los sectores: médicos, abogados, secretarias, ingenieros… Casi todos eran padres de alumnos que nos contaban sus experiencias. Hasta creo recordar que vino Jose Antonio Corbalán y metió una canasta desde el otro lado del campo después de la charla. ¡Nos quedamos alucinados!</p>

